# arithmetic
x = 15
y = 4
print('x + y =', x + y)
print('x - y =', x - y)
print('x * y =', x * y)
print('x / y =', x / y)
print('x // y =', x // y)
print('x ** y =', x ** y)

# logical
a = True
b = False
print('a and b is', a and b)
print('a or b is', a or b)
print('not a is', not a)

# membership
i = 'Hello world'
j = {1: 'a', 2: 'b'}
print('H' in i)
print('hello' not in i)
print(1 in j)
print('a' in j)

# bitwise
m = 10
n = 4
print(m & n)
print(m | n)
print(~m)
print(m ^ n)
print(m >> 2)
print(m << 2)
